package uk.ac.cam.ceb.como.extra.xom;

/**
 *
 * @author pb556
 */
public class XMLException extends Exception {

    public XMLException(Throwable cause) {
        super(cause);
    }

    public XMLException(String message, Throwable cause) {
        super(message, cause);
    }

    public XMLException() {
        super();
    }

    public XMLException(String string) {
        super(string);
    }
}

package com.cmclinnovations.jps.kg.object.model;

public class Job {
	private String levelOfTheory;
	private String keyword;
	private String algorithmChoice;
	public String getLevelOfTheory() {
		return levelOfTheory;
	}
	public void setLevelOfTheory(String levelOfTheory) {
		this.levelOfTheory = levelOfTheory;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public String getAlgorithmChoice() {
		return algorithmChoice;
	}
	public void setAlgorithmChoice(String algorithmChoice) {
		this.algorithmChoice = algorithmChoice;
	}
}

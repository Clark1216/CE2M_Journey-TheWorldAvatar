def get_el_symbol_by_atomic_nr(el_atnr):
    rDat = ''
    for key,value in EL_DATA.items():
        if value[EL_NR_COL] == el_atnr:
            rDat = key
            break
    return rDat

def getCompMolWt(aComp):
    MolWt = 0.0
    for i in range(0,len(aComp),2):
        ElWt = get_el_wt_by_symbol(aComp[i])
        MolWt = MolWt + ElWt*aComp[i+1]
    return MolWt
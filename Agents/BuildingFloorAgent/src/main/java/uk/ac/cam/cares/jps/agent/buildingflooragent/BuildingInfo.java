package uk.ac.cam.cares.jps.agent.buildingflooragent;

public class BuildingInfo {
    
    private String type;
    private int floors;

    public String getType() {
        return this.type;
    }

    public int getFloors() {
        return this.floors;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setFloors(int floors) {
        this.floors = floors;
    }
}
